#scrape de cotações
scrapef() {
	#check for user-set separator
	[[ -n "$USRSEP" ]] && SEP="$USRSEP" || SEP='\t'
	procf() {
		sed 's/\r//g' | jq -r '.docs|reverse[]|"\(.abbreviation//.name)'$SEP'\(.price//.askvalue)'$SEP'\(.high//.maxbid//"-")'$SEP'\(.low//.minbid//"-")'$SEP'\(.open//"-")'$SEP'\(.close//"-")'$SEP'\(.change//.variationbid//"-")'$SEP'\(.pctChange//.variationpercentbid//"-")%'$SEP'\(.date)'$SEP'\(.exchangeasset//"")"'
	}
	loopf() {
		while ((f<=max)); do
			g="$(eval printf '%s,' {$i..$f})"; i=$((i+inc)); f=$((f+inc))
			printf '\033[2K\r' 1>&2  #clear stderr, only interactive
			cf || { sleep 11; cf;} || { sleep 22; cf;} 
			printf 'Aguarde..  %s\r' "(${f}+${inc})/${max}" 1>&2  #loading bar, only interactive
			sleep 0.9
		done
	}
	
	#cabeçalho
	cat <<-!
	UOL - Economia
	$(date)
	
	COLUNAS
	Moedas___: Nome,Preço,Max,Min,Var,Var%%,Data
	Ações____: Símbolo,Pontos,Alta,Baixa,Abertura,Fechamento,Var,Var%%,Data,Nome
	Separador: ${USRSEP:-<TAB>}

	!

	printf 'Aguarde..\r' 1>&2
	set -o pipefail
	
	{
	#currency rates
	cf() { ${YOURAPP} "https://api.cotacoes.uol.com/mixed/summary?&currencies=${g%,}&fields=name,openbidvalue,askvalue,variationpercentbid,price,exchangeasset,open,pctChange,date,abbreviation&json=json" | procf;}
	i=1; f=25; inc=$f; max=175
	loopf

	#stocks and indexes
	cf() { ${YOURAPP} "https://api.cotacoes.uol.com/mixed/summary?&itens=${g%,}&fields=name,openbidvalue,askvalue,variationpercentbid,price,exchangeasset,open,pctChange,date,abbreviation&json=json" | procf;}
	i=1; f=9; inc=$f; max=2010
	loopf
	} | sed -E 's/([0-9]{4})([0-9]{2})([0-9]{2})([0-9]{2})([0-9]{2})([0-9]{2})/\1-\2-\3T\4:\5:\6/'

}
