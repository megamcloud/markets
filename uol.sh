	uol.sh [-ahllmv]

	
	O script puxa as cotações de páginas de economia do UOL.

	Para uma liste simples com símbolos para algumas ações e seus nomes, use
	a opção '-l'. Para imprimir uma lista com os nomes, símbolos e números 
	de identificação do UOL, use a opção '-ll'. Porém, observe que esta lis-
	tagem é fixa e não puxa dados ao vivo.
	
	Para puxar o valor de todos os ativos disponíveis (scrape), utilize a
	opção '-a'. É uma lista grande e demorada para ser impressa. Copie os 
	dados e faça uma tabela com as informações de formatação oferecidas no
	cabeçalho.

	O número de identificação ID pode ser usado para puxar um ticker resu-
	mido do ativo correspondente. Veja a seção seguinte sobre tickers.

	A opção '-a' tenta puxar todas as cotações de moedas, ações e índices
	suportados pelo UOL. O 'scrape' de dados é uma operação lenta.

	Os pacotes Bash, cURL ou Wget e gzip são necessários.


TICKER RESUMIDO
	Há um hack para puxar o ticker resumido de um ativo disponível do UOL.
	Tente digitar o nome da ação, moeda ou índice como argumentos para o 
	programa:

		$ uol.sh PETR4.SA

	Se não funcionar, será necessário que se utilize o número de ID do ativo
	para este fim. Para pegar o número de ID de uma ação, moeda ou índice, 
	utilize a opção '-ll' (esta lista não é atualizada em tempo real) para
	pegar o número de ID da ação ou moeda de interesse. Por exemplo, com o
	comando:

		$ uol.sh -ll  |  grep -i petrobras
	

	Verificamos que as ações PETR4.SA e PETR3.SA possuem o número de ID 484 
	e 485, respectivamente. Para puxar um ticker resumido:
	
		$ uol.sh 484
	

	Em caso de colisão de números de identificação entre moedas, ações e 
	índices, será puxado ambos (números 1-170). Por exemplo, a cotação do 
	Euro em reais tem ID número 5. Porém, o índice 'Special Governance Cor-
	porate Governance Stock Index', IGCX, também tem seu número de ID 5.
	Neste caso, será impresso tickeres para ambos.
	

