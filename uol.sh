	      		;;
		m ) #cotações metais
			METAISOPT=1
	      		;;
		h ) # Help
	      		echo -e "${AJUDA}"
	      		exit 0
	      		;;
		v ) # Version of Script
	      		grep -m1 '# v' "${0}"
	      		exit 0
	      		;;
		\? )
	     		printf "Opção inválida: -%s\n" "${OPTARG}" 1>&2
	     		exit 1
	     		;;
  	esac
done
shift $((OPTIND -1))

#Call opts
if [[ -n "${B3OPT}" ]]; then
	b3f 
elif [[ -n "${DOLAROPT}" ]]; then
	dolarf 
